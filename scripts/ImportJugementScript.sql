    ),
    principal = (
        SELECT principal
        FROM temp_judgment_updates u
        WHERE u.account_no = customers.account_no
    ),
    judgment_difference = (
        SELECT ROUND(
            CASE
                WHEN total_debt - principal < 0 THEN 0
                ELSE total_debt - principal
            END
            + court_fee
            + lawyer_fee,
            2
        )
        FROM temp_judgment_updates u
        WHERE u.account_no = customers.account_no
    ),
    interest_rate = (
        SELECT interest_rate
        FROM temp_judgment_updates u
        WHERE u.account_no = customers.account_no
